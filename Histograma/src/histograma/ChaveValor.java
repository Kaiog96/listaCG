
package histograma;


public class ChaveValor {

    private final Integer chave;
    private final Integer valor;

    public ChaveValor(Integer chave, Integer valor) {
        this.chave = chave;
        this.valor = valor;
    }

    public Integer getChave() {
        return chave;
    }

    public Integer getValor() {
        return valor;
    }

}
